package com.aspire.java.oct_dec21Logicalprog;

public class For_OddNumber 
{

	public static void main(String[] args) 
	{
		for(int i=1; i<=50; i=i+2)
		{
			System.out.print(i+", ");
		}
		System.out.println();
		
		//rev
		
		for(int j=49; j>0; j=j-2)
		{
			System.out.print(j+", ");
		}
		
		//
		
	
	}
	
	
}
