package com.aspire.java.logicalLoops;

public class ForLoop2 {
	
	public static void main(String[]args)
	{
		//50 to 100 sum no
		
		int i=50;
		
		for(i=50; i<=100; i=i+2)
			
		{
			System.out.println(i);
			
		}
		
		
		even();
	}
	
	public static void even() {
		
		int i=50;
		for (i=50; i<=100; i=i+2)
			
		{
			System.out.println(i);
		}
	}

}
