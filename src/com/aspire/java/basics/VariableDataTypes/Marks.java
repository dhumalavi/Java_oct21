package com.aspire.java.basics.VariableDataTypes;

public class Marks {
	
	public static void main(String[] args) {
		
		byte  a=20;
		
		byte  b=18;
		
		byte  c=19;
		
		byte  d=15;
		
		long e =12345698745632145L;
		
		System.out.println("Marks");
		
		System.out.println("1) English = " +a);
		
		System.out.println("2) Marathi = " +b);
		
		System.out.println("3) Hindi = " +c);
		
		System.out.println("4) History = " +d);
		
		System.out.println("*) long = " +e);
		
	}

}
