package com.aspire.java.Constructor;

public class UserDefinedCon {
	
	int a,b,c;
	char i;
	float j;
	
	UserDefinedCon()
	{
		a=12;
		b=20;
		c=22;
		i='*';
		j=1.25f;
		
	}
	
	public void method1()
	{
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(i);
		System.out.println(j);

		}

}
